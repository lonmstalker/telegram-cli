struct TestRepo {
    root: PathBuf,
}

impl TestRepo {
    fn new() -> Self {
        static NEXT: AtomicUsize = AtomicUsize::new(0);
        let unique = NEXT.fetch_add(1, Ordering::Relaxed);
        let root = std::env::temp_dir().join(format!(
            "tdlib-registry-gen-test-{}-{unique}",
            std::process::id()
        ));
        if root.exists() {
            fs::remove_dir_all(&root).expect("remove stale test root");
        }
        fs::create_dir_all(root.join("vendor/tdlib")).expect("vendor directory");
        fs::create_dir_all(root.join("policy")).expect("policy directory");
        fs::create_dir_all(root.join("generated")).expect("generated directory");
        Self { root }
    }

    fn write_inputs(&self) {
        fs::write(self.root.join("vendor/tdlib/td_api.tl"), SCHEMA).expect("schema");
        fs::write(
            self.root.join("vendor/tdlib/manifest.json"),
            serde_json::to_vec(&json!({
                "format_version": 1,
                "upstream": {
                    "repository": "https://example.invalid/tdlib",
                    "commit": "0123456789abcdef0123456789abcdef01234567",
                    "version": "test"
                },
                "schema": {
                    "sha256": sha256_hex(SCHEMA.as_bytes()),
                    "definitions": 5,
                    "functions": 4,
                    "updates": 0,
                    "authorization_states": 0
                }
            }))
            .expect("manifest JSON"),
        )
        .expect("manifest");
        fs::write(
            self.root.join("policy/tdlib-feature-owners.json"),
            serde_json::to_vec(&json!({
                "format_version": 1,
                "schema_sha256": sha256_hex(SCHEMA.as_bytes()),
                "rules": [
                    feature_rule(
                        "F007",
                        json!([
                            { "kind": "exact", "value": "setUserName" },
                            { "kind": "contains", "value": "User" }
                        ]),
                        2,
                        USER_SET_SHA256,
                        "getUser",
                        "getChat"
                    ),
                    feature_rule(
                        "F008",
                        json!([{ "kind": "contains", "value": "Chat" }]),
                        2,
                        CHAT_SET_SHA256,
                        "getChat",
                        "getUser"
                    )
                ],
                "overrides": []
            }))
            .expect("policy JSON"),
        )
        .expect("policy");
    }

    fn change_policy_rationale(&self) {
        let path = self.root.join("policy/tdlib-feature-owners.json");
        let mut policy: serde_json::Value =
            serde_json::from_slice(&fs::read(&path).expect("policy")).expect("policy JSON");
        policy["rules"][0]["rationale"] =
            json!("Updated reviewed rationale must change canonical policy evidence.");
        fs::write(
            path,
            serde_json::to_vec(&policy).expect("updated policy JSON"),
        )
        .expect("updated policy");
    }
}

impl Drop for TestRepo {
    fn drop(&mut self) {
        let _ = fs::remove_dir_all(&self.root);
    }
}

fn feature_rule(
    feature_id: &str,
    any: serde_json::Value,
    method_count: usize,
    method_set_sha256: &str,
    positive: &str,
    negative: &str,
) -> serde_json::Value {
    json!({
        "feature_id": feature_id,
        "any": any,
        "expected": {
            "method_count": method_count,
            "method_set_sha256": method_set_sha256
        },
        "positive_examples": [positive],
        "negative_examples": [negative],
        "rationale": "Fixture rule with explicit positive and negative evidence."
    })
}

fn sha256_hex(bytes: &[u8]) -> String {
    let digest = Sha256::digest(bytes);
    let mut encoded = String::with_capacity(digest.len() * 2);
    for byte in digest {
        write!(&mut encoded, "{byte:02x}").expect("writing to String cannot fail");
    }
    encoded
}

fn temp_path(output: &Path) -> PathBuf {
    PathBuf::from(format!("{}{TEMP_SUFFIX}", output.display()))
}
